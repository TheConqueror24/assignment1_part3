    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Login login = new Login();
        
        
        Register_Form regform = new Register_Form();
        String username = UsernameRField.getText();
        String password = new String(passwordRField.getPassword());
        
      
        
        boolean isSuccess = login.loginUser(username, password);

        if (isSuccess) {
        // Call the Message class's sentMessage()
        Message message = new Message(
            "dummyID",   // Placeholder messageID (will be overridden in sentMessage)
            Register_Form.phoneNumber, // Placeholder recipient
            "dummyMessage",   // Placeholder message
            username,    // Use the logged-in username as sender
            new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date()) // Timestamp
        );
        
       
        String status = message.sentMessage();
        JOptionPane.showMessageDialog(null, "Message Status: " + status);
    } else {
        String statusMessage = login.returnLoginStatus(false);        
        JOptionPane.showMessageDialog(null, statusMessage);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

